	/// <summary>
	/// This sample demonstrates listening for change events when ground to grid properties are altered for the active map. 
	/// </summary>
	/// <remarks>
	/// 1. Build or debug the sample through Visual Studio.
	/// 2. In Pro, select the Add-In ribbon and click the button Show Ground To Grid Events Pane.
	/// ![UI](Screenshots/Screen1.png)
	/// 3. On the dockable pane click the button Start Events.
	/// 4. Using the Ground to grid Corrections tray on the active map, change any property of the ground to grid corrections.
	/// 5. The code detects the change and writes the previous value and new value to the Ground to Grid Events pane.
	/// ![UI](Screenshots/Screen2.png)
	/// </remarks>

	internal class Module1 : Module
	{
		private static Module1 _this = null;

		/// <summary>
		/// Retrieve the singleton instance to this module here
		/// </summary>
		public static Module1 Current
		{
			get
			{
				return _this ?? (_this = (Module1)FrameworkApplication.FindModule("G2GEvents_Module"));
			}
		}

		#region Overrides
		/// <summary>
		/// Called by Framework when ArcGIS Pro is closing
		/// </summary>
		/// <returns>False to prevent Pro from closing, otherwise True</returns>
		protected override bool CanUnload()
		{
			//TODO - add your business logic
			//return false to ~cancel~ Application close
			return true;
		}

		#endregion Overrides

	}
